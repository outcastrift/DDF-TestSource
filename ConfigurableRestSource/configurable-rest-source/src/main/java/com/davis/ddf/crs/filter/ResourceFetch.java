package com.davis.ddf.crs.filter;

/**
 * A Fetch is used to fetch a specific record, using an identifier
 *
 * @author Bob Harrod
 * @version 1.0
 */
public class ResourceFetch {
  private String id;

  /**
   * Gets id.
   *
   * @return the id
   */
  public String getId() {
    return id;
  }

  /**
   * Sets id.
   *
   * @param id the id to set
   */
  public void setId(String id) {
    this.id = id;
  }


}
